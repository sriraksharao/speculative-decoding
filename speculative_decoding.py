N_REPEATS  = 2

# AR baseline
print('Running AR baseline...')
ar_tps = []
for p in PROMPTS:
    for _ in range(N_REPEATS):
        r = autoregressive_decode(p, max_new_tokens=MAX_TOKENS, **SAMPLE_KWARGS)
        ar_tps.append(r.tokens_per_sec)
