# Gamma sweep
gamma_rows = []
for g in GAMMAS:
    print(f'  gamma={g}...', end='', flush=True)
    tps_list, acc_list = [], []
    for p in PROMPTS:
        for _ in range(N_REPEATS):
            r = speculative_decode(p, max_new_tokens=MAX_TOKENS, gamma=g, **SAMPLE_KWARGS)
            tps_list.append(r.tokens_per_sec)
            acc_list.append(r.acceptance_rate)
